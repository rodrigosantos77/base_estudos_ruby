#Usar operadores de atribuição para modificar o valor de uma variável:


valor = 10
puts "Valor inicial: #{valor}"

valor += 5
puts "Após adição: #{valor}"

valor *= 2
puts "Após multiplicação: #{valor}"

valor -= 4
puts "Após subtração: #{valor}"

valor /= 2
puts "Após divisão: #{valor}"

valor %= 3
puts "Após módulo: #{valor}"
